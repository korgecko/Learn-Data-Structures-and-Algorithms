# 메모이제이션을 위한 리스트(배열) d를 100칸 만들어 초기값은 모두 0으로 설정
# d[i]에는 '피보나치 수열의 i번째 값'이 저장될 예정
d = [0] * 100


def fibo(x):
    # 1번째, 2번째 피보나치 수는 항상 1
    if x == 1 or x == 2:
        return 1

    # 이미 계산한 적이 있다면 그 값을 그대로 반환 (중복 연산 방지)
    if d[x] != 0:
        return d[x]

    # 아직 계산하지 않았다면, 재귀 호출을 통해 값을 구한 뒤 배열에 저장
    d[x] = fibo(x - 1) + fibo(x - 2)

    # 최종적으로 계산된 값을 반환
    return d[x]


# 99번째 피보나치 수를 출력
print(fibo(10))
print(fibo(99))
