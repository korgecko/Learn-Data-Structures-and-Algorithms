# 메모이제이션(저장)을 위한 리스트 d를 100칸 준비, 초기값은 모두 0
d = [0] * 100

# 피보나치 수열의 첫 번째와 두 번째 값은 1로 정의
d[1] = 1
d[2] = 1

# 첫 번째 계산: n = 10번째 피보나치 수 구하기
n = 10

# 3번째 항부터 n번째 항까지 반복문을 돌면서 점화식 적용
for i in range(3, n + 1):
    d[i] = d[i - 1] + d[i - 2]  # 이전 두 항을 더해서 현재 항 저장

# d[n]에는 10번째 피보나치 수가 저장됨 → 출력
print(d[n])   # 결과: 55

# 두 번째 계산: n = 99번째 피보나치 수 구하기
n = 99

# 3번째 항부터 99번째 항까지 차례대로 계산
for i in range(3, n + 1):
    d[i] = d[i - 1] + d[i - 2]

# d[n]에는 99번째 피보나치 수가 저장됨 → 출력
print(d[n])   # 매우 큰 수 출력됨 (Python은 큰 정수도 자동 지원)
