#include <iostream>
#include <queue>   // queue를 쓰려면 이 헤더 필요!

using namespace std;

queue<int> q;   // int형 데이터를 저장하는 큐 선언 (FIFO 구조)

int main(void) {
    q.push(5);   // 큐에 5 삽입 → [5]
    q.push(2);   // 큐에 2 삽입 → [5, 2]
    q.push(3);   // 큐에 3 삽입 → [5, 2, 3]
    q.push(7);   // 큐에 7 삽입 → [5, 2, 3, 7]

    q.pop();     // 큐의 맨 앞(5) 제거 → [2, 3, 7]

    q.push(1);   // 큐에 1 삽입 → [2, 3, 7, 1]
    q.push(4);   // 큐에 4 삽입 → [2, 3, 7, 1, 4]

    q.pop();     // 큐의 맨 앞(2) 제거 → [3, 7, 1, 4]

    // 이제 while문 시작
    while (!q.empty()) {            // 큐가 비어있지 않으면 계속 반복
        cout << q.front() << ' ';   // 큐의 맨 앞 요소 출력
        q.pop();                    // 맨 앞 요소 제거
    }

    // 반복 과정:
    // 출력: 3 → 큐 [7, 1, 4]
    // 출력: 7 → 큐 [1, 4]
    // 출력: 1 → 큐 [4]
    // 출력: 4 → 큐 []

    // 최종 출력 결과: "3 7 1 4"
}
